ip1 = int(input("Informe primeira parte do ip [\033[7;30mXXX\033[m.000.000.000]:"))
ip2 = int(input("Informe segunda  parte do ip [000.\033[7;30mXXX\033[m.000.000]:"))
ip3 = int(input("Informe terceira parte do ip [000.000.\033[7;30mXXX\033[m.000]:"))
ip4 = int(input("Informe quarta   parte do ip [000.000.000.\033[7;30mxxx\033[m]:"))
a = bin(ip1)
b = bin(ip2)
c = bin(ip3)
d = bin(ip4)
print("O ip {}.{}.{}.{} corresponte a {}.{}.{}.{} em octal.".format(ip1,ip2,ip3,ip4,a[2:].zfill(8),b[2:].zfill(8),c[2:].zfill(8),d[2:].zfill(8)))
